import { readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const pkgRoot = resolve(here, "..", "node_modules", "material-icon-theme");
const manifest = JSON.parse(readFileSync(resolve(pkgRoot, "dist", "material-icons.json"), "utf8"));

const EXTENSIONS = [
  "ts", "tsx", "js", "jsx", "mjs", "cjs", "json", "jsonc",
  "py", "rb", "go", "rs", "java", "kt", "swift", "php", "cs",
  "c", "h", "cpp", "hpp", "cc", "scala", "clj", "ex", "exs", "lua", "dart", "r",
  "html", "htm", "xml", "vue", "svelte", "astro",
  "css", "scss", "sass", "less", "styl",
  "md", "markdown", "mdx", "txt", "pdf", "rtf",
  "yaml", "yml", "toml", "ini", "conf", "env",
  "sh", "bash", "zsh", "fish", "ps1", "bat",
  "sql", "prisma", "graphql", "gql",
  "png", "jpg", "jpeg", "gif", "webp", "bmp", "ico", "svg",
  "zip", "tar", "gz", "rar", "7z",
  "lock", "wasm", "csv", "log",
];

const FILENAMES = [
  "package.json", "tsconfig.json", "dockerfile", "docker-compose.yml",
  "makefile", "license", "readme.md", ".gitignore", ".gitattributes",
  ".eslintrc", ".prettierrc", "vite.config.js", "cargo.toml",
  "go.mod", "requirements.txt", ".npmrc", ".editorconfig",
];

const DEFAULT_FILE = manifest.file;
const DEFAULT_FOLDER = manifest.folder;

function svgFor(iconName) {
  const def = manifest.iconDefinitions[iconName];
  if (!def) return null;
  const file = resolve(pkgRoot, "dist", def.iconPath);
  let svg = readFileSync(file, "utf8").trim();
  svg = svg.replace(/\s*xml:space="preserve"/g, "");
  return svg;
}

function iconNameForExt(ext) {
  return manifest.fileExtensions[ext] ?? manifest.languageIds?.[ext] ?? DEFAULT_FILE;
}
function iconNameForName(name) {
  return manifest.fileNames[name] ?? DEFAULT_FILE;
}

const icons = {};
const byExt = {};
const byName = {};

function register(iconName) {
  if (iconName && !icons[iconName]) {
    const svg = svgFor(iconName);
    if (svg) icons[iconName] = svg;
    else return DEFAULT_FILE;
  }
  return iconName;
}

for (const ext of EXTENSIONS) {
  const name = iconNameForExt(ext);
  if (name !== DEFAULT_FILE) byExt[ext] = register(name);
}
for (const name of FILENAMES) {
  const icon = iconNameForName(name);
  if (icon !== DEFAULT_FILE) byName[name] = register(icon);
}
register(DEFAULT_FILE);
register(DEFAULT_FOLDER);

const out = `// @generated by scripts/gen-material-icons.mjs — do not edit by hand.
// Subset of Material Icon Theme (MIT, material-extensions/vscode-material-icon-theme).
// Run \`npm run gen-icons\` to regenerate.

export const DEFAULT_FILE = ${JSON.stringify(DEFAULT_FILE)};
export const DEFAULT_FOLDER = ${JSON.stringify(DEFAULT_FOLDER)};

// icon name -> raw SVG markup
export const ICON_SVG = ${JSON.stringify(icons, null, 2)};

// file extension (lowercase, no dot) -> icon name
export const BY_EXTENSION = ${JSON.stringify(byExt, null, 2)};

// exact filename (lowercase) -> icon name
export const BY_FILENAME = ${JSON.stringify(byName, null, 2)};
`;

const target = resolve(here, "..", "src", "lib", "material-icons.generated.js");
writeFileSync(target, out);
console.log(
  `Wrote ${target}\n  ${Object.keys(icons).length} icons, ` +
    `${Object.keys(byExt).length} extensions, ${Object.keys(byName).length} filenames`,
);
